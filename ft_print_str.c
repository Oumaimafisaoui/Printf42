#include "ft_printf.h"

int	ft_print_str(char *str)
{
	int index;

	index = 0;
	if (!str)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (*str)
	{
		write(1, str, 1);
		str++;
		index++;
	}
	return (index);
}
